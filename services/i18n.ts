/**
 * Get translations for a language code.
 * Falls back to English for unknown languages.
 */
export function getTranslations(lang?: string | null): Translations {
  if (!lang) return translations.en;
  const code = lang.slice(0, 2).toLowerCase() as Language;
  return translations[code] || translations.en;
}

/**
 * Get language from URL params (?lang=es) or browser preference
 */
export function detectLanguage(): Language {
  // Check URL param first
  const params = new URLSearchParams(window.location.search);
  const langParam = params.get('lang');
  if (langParam && translations[langParam.slice(0, 2) as Language]) {
    return langParam.slice(0, 2) as Language;
  }

  // Check browser language
  const browserLang = navigator.language?.slice(0, 2) as Language;
  if (translations[browserLang]) {
    return browserLang;
  }

  return 'en';
}

